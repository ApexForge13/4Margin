// ── County-Level Jurisdiction Database — MD & PA ────────────────────────────
//
// County-granularity data for building code enforcement: climate zones,
// design wind speeds, ice barrier scope, permit fees, and AHJ contacts.
//
// Resolves from property ZIP code (already collected on claims).
// Falls back to state-level data when no county match is found.
//
// Sources:
//   IECC Figure R301.1 — Climate Zone Map
//   ASCE 7-16 Figure 26.5-1B — Basic Wind Speed Map
//   County AHJ websites / permit fee schedules (2024-2025)
//
// PHASE 1: MD (all 24 jurisdictions) + PA (14 priority counties)
// ────────────────────────────────────────────────────────────────────────────

export interface CountyJurisdiction {
  /** County name (e.g., "Montgomery") */
  county: string;
  /** State abbreviation */
  state: "MD" | "PA";
  /** IECC Climate Zone — drives ice barrier scope */
  climateZone: "4A" | "5A" | "6A";
  /** Design wind speed in mph per ASCE 7-16 */
  designWindSpeed: number;
  /** True if design wind speed >= 120 mph (6-nail pattern required) */
  highWindZone: boolean;
  /** Ice barrier installation scope per climate zone + local practice */
  iceBarrierRequirement:
    | "eaves_only"
    | "eaves_valleys_penetrations"
    | "eaves_valleys_penetrations_extended";
  /** Permit information */
  permit: {
    required: boolean;
    typicalFeeRange: string;
    /** Authority Having Jurisdiction name */
    ahjName: string;
    ahjPhone: string | null;
    ahjUrl: string | null;
    notes: string | null;
  };
  /** County-specific code amendments or requirements beyond state code */
  localAmendments: string[];
  /** FIPS county code */
  fipsCode: string;
}

