/**
 * Resolve a ZIP code to a CountyJurisdiction.
 * Returns null if the ZIP is not in our database (unknown county).
 */
export function resolveCountyFromZip(
  zip: string | null | undefined
): CountyJurisdiction | null {
  if (!zip) return null;

  // Normalize to 5-digit
  const zip5 = zip.trim().slice(0, 5);
  if (zip5.length !== 5) return null;

  const mapping = ZIP_TO_COUNTY[zip5];
  if (!mapping) return null;

  const county = ALL_COUNTIES.find(
    (c) => c.county === mapping.county && c.state === mapping.state
  );

  return county || null;
}

/**
 * Resolve county from county name + state (for cases where we already know the county).
 */
export function resolveCountyByName(
  countyName: string,
  state: "MD" | "PA"
): CountyJurisdiction | null {
  if (!countyName || !state) return null;

  const normalized = countyName.trim().toLowerCase();
  return (
    ALL_COUNTIES.find(
      (c) =>
        c.state === state &&
        c.county.toLowerCase() === normalized
    ) || null
  );
}

/**
 * Get a human-friendly description of ice barrier scope.
 */
export function iceBarrierScopeLabel(
  requirement: CountyJurisdiction["iceBarrierRequirement"]
): string {
  switch (requirement) {
    case "eaves_only":
      return "eaves only (24\" past interior wall line)";
    case "eaves_valleys_penetrations":
      return "eaves, valleys, and around all penetrations";
    case "eaves_valleys_penetrations_extended":
      return "eaves (36\" past interior wall line), valleys, and around all penetrations";
    default:
      return "per local requirements";
  }
}

/**
 * Build county-specific context string for injection into Claude's analysis prompt.
 * Returns empty string if no county data is available.
 */
export function buildCountyContextForPrompt(
  county: CountyJurisdiction
): string {
  const lines: string[] = [
    `## COUNTY-SPECIFIC JURISDICTION DATA`,
    `County: ${county.county}, ${county.state}`,
    `FIPS Code: ${county.fipsCode}`,
    `Climate Zone: IECC ${county.climateZone}`,
    `Design Wind Speed: ${county.designWindSpeed} mph (ASCE 7-16)`,
    `High Wind Zone: ${county.highWindZone ? "YES — 6-nail fastening pattern REQUIRED" : "No — standard 4-nail pattern"}`,
    `Ice Barrier Scope: ${iceBarrierScopeLabel(county.iceBarrierRequirement)}`,
    "",
    `### Permit Information`,
    `Permit Required: ${county.permit.required ? "YES" : "No"}`,
    `Typical Fee Range: ${county.permit.typicalFeeRange}`,
    `Authority Having Jurisdiction (AHJ): ${county.permit.ahjName}`,
  ];

  if (county.permit.ahjPhone) {
    lines.push(`AHJ Phone: ${county.permit.ahjPhone}`);
  }
  if (county.permit.ahjUrl) {
    lines.push(`AHJ Website: ${county.permit.ahjUrl}`);
  }
  if (county.permit.notes) {
    lines.push(`Note: ${county.permit.notes}`);
  }

  if (county.localAmendments.length > 0) {
    lines.push("");
    lines.push(`### Local Amendments & Requirements`);
    for (const amendment of county.localAmendments) {
      lines.push(`- ${amendment}`);
    }
  }

  lines.push("");
  lines.push(
    "IMPORTANT: Use this county-specific data when justifying ice barrier scope, fastener patterns, and permit fees. " +
    `Cite "${county.county} County, ${county.state}" as the jurisdiction in your justifications.`
  );

  return lines.join("\n");
}
